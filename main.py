import threading

# –§—É–Ω–∫—Ü—ñ—è –¥–ª—è —Ç–∞–π–º-–∞—É—Ç—É
def timeout():
    print("\n‚è∞ –ß–∞—Å –≤–∏–π—à–æ–≤!")
    exit()

# –í–≤–µ–¥–µ–Ω–Ω—è –≤—ñ–¥—Å–æ—Ç–∫–∞, —è–∫–∏–π –±—É–¥–µ –≤—ñ–¥–Ω—ñ–º–∞—Ç–∏—Å—è
x = input("–í–≤–µ–¥—ñ—Ç—å –≤—ñ–¥—Å–æ—Ç–∫–∏, —è–∫—ñ –±—É–¥—É—Ç—å –≤—ñ–¥–Ω—ñ–º–∞—Ç–∏—Å—è: ")
try:
    x = float(x)
    # –§–æ—Ä–º—É–ª–∞ –¥–ª—è —Ä–æ–∑—Ä–∞—Ö—É–Ω–∫—É —Å–∫—ñ–ª—å–∫–∏ –≤—ñ–¥—Å–æ—Ç–∫—ñ–≤ —Ç—Ä–µ–±–∞ –¥–æ–¥–∞—Ç–∏
    y = (x * 0.01) / (0.01 - x * 0.0001)
    print(f"üëâ –©–æ–± –ø—ñ—Å–ª—è –≤—ñ–¥–Ω—ñ–º–∞–Ω–Ω—è {x:.2f}% –∑–∞–ª–∏—à–∏–ª–∞—Å—å —Å—É–º–∞ —è–∫ –±—É–ª–∞, –ø–æ—Ç—Ä—ñ–±–Ω–æ –ø–æ–ø–µ—Ä–µ–¥–Ω—å–æ –¥–æ–¥–∞—Ç–∏: {y:.2f}%")
except ValueError:
    print("‚ùå –ü–æ–º–∏–ª–∫–∞: –≤–≤–µ–¥—ñ—Ç—å —á–∏—Å–ª–æ")
    exit()

# –ó–∞–ø—É—Å–∫ —Ç–∞–π–º–µ—Ä–∞ –Ω–∞ 15 —Å–µ–∫—É–Ω–¥
time = threading.Timer(15.0, timeout)
time.start()

# –í–≤–µ–¥–µ–Ω–Ω—è —Å—É–º–∏
s = input("–í–≤–µ–¥—ñ—Ç—å —Å—É–º—É: ")
time.cancel()

# –†–æ–∑—Ä–∞—Ö—É–Ω–æ–∫ –Ω–æ–≤–æ—ó —Å—É–º–∏
try:
    a = float(s)
    result = a + 0.01 * a * y
    print(f"\nüìå –ü–æ—á–∞—Ç–∫–æ–≤–∞ —Å—É–º–∞: {a:.2f} –≥—Ä–Ω")
    print(f"‚ûï –ù–µ–æ–±—Ö—ñ–¥–Ω–æ –¥–æ–¥–∞—Ç–∏ {y:.2f}% ‚Üí {result:.2f} –≥—Ä–Ω")
except ValueError:
    print("‚ùå –ü–æ–º–∏–ª–∫–∞: –≤–≤–µ–¥—ñ—Ç—å —á–∏—Å–ª–æ")
    exit()
